package logicadenegocios;

public class Hotel {
	private int cantidadCuartos;
	private int cantidadBaños;
	private int cantidadRestaurantes;
	private int cantidadPiscinas;
	
	public Hotel() {
		
		
	}

	public int getCantidadCuartos() {
		return cantidadCuartos;
	}

	public void setCantidadCuartos(int pCantidadCuartos) {
		this.cantidadCuartos = pCantidadCuartos;
	}

	public int getCantidadBaños() {
		return cantidadBaños;
	}

	public void setCantidadBaños(int pCantidadBaños) {
		this.cantidadBaños = pCantidadBaños;
	}

	public int getCantidadRestaurantes() {
		return cantidadRestaurantes;
	}

	public void setCantidadRestaurantes(int pCantidadRestaurantes) {
		this.cantidadRestaurantes = pCantidadRestaurantes;
	}

	public int getCantidadPiscinas() {
		return cantidadPiscinas;
	}

	public void setCantidadPiscinas(int pCantidadPiscinas) {
		this.cantidadPiscinas = pCantidadPiscinas;
	}
	

}
