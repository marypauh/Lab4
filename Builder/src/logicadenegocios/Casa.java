package logicadenegocios;

public class Casa {
	private int cuartos;
	private int banos;
	private int garajes;
	
	public Casa() {
		
		
	}
	
	public void setCuartos(int pCuartos) {
		cuartos = pCuartos;
	}
	
	public void setBanos(int pBanos) {
		banos = pBanos;
	}
	
	public void setGarajes(int pGarajes) {
		garajes = pGarajes;
	}

	public int getCuartos() {
		return cuartos;
	}

	public int getBanos() {
		return banos;
	}

	public int getGarajes() {
		return garajes;
	}
	
	

}
